<template>
  <div>
    <v-snackbar
      v-model="configuration.show"
      :top="true"
      :right="true"
      :multi-line="true"
      :timeout="-1"
      :color="configuration.color"
    >
      {{ configuration.message }}
      <v-btn color="white" text @click="close()"> Close </v-btn>
    </v-snackbar>
  </div>
</template>

<script>
import Config from '../services/config'
export default {
  computed: {
    configuration() {
      return this.$store.state.globalSnackbarSettings
    },
  },
  methods: {
    close() {
      this.$store.commit(Config.mutations.global.HIDE_SNACKBAR)
    },
  },
}
</script>
